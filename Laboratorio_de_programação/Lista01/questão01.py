#entrada
print('para calculara a velocidade media entre com os valores.')
km_rodados = float(input('Escreva quantos km foram percorridos: '))
temp = float(input('Digite quanto tempo gasto no percursso: '))

#processamento 
vel_media = (km_rodados  / temp)

#saida
print(f'Sua velocida media nessa viagem sera de {vel_media} Km/h.')
